<script>
document.addEventListener('DOMContentLoaded', function(){
  let swiperPc,
      swiperAll,
      swiperSp;
  // 常時有効
  function initializeSwiperAll(){
    swiperAll = new Swiper('.swiper--all',{
      spaceBetween: 20,
      loop: true,
      centeredSlides: true,
      slidesPerView: 1.25,
      navigation:{
        nextEl: ".swiper-button-next",
        prevEl: ".swiper-button-prev"
      },
      pagination: {
        el: ".swiper-pagination",
        type: "progressbar",
      },
      breakpoints: {
        768: {
          loop: false,
          centeredSlides: false,
          slidesPerView: 3.05,
          spaceBetween: 30,
        },
      },
    });
  }
  // 有効化時のオプションを記述
  function initializeSwiperPc(){
    swiperPc = new Swiper('.swiper--pc',{
      spaceBetween: 30,
      slidesPerView: 3.25,
      // loop: true,
      navigation:{
        nextEl: ".swiper-button-next",
        prevEl: ".swiper-button-prev"
      },
    });
  }
  function initializeSwiperSp(){
    swiperSp = new Swiper('.swiper--sp',{
      spaceBetween: 20,
      slidesPerView: 2.3,
      loop: true,
      pagination: {
        el: ".swiper-pagination",
        type: "progressbar",
      },
    });
  }
  // 無効化する用の記述
  function destroySwiperPc(){
    if(swiperPc){
      swiperPc.destroy();
      swiperPc = undefined;
    }
  }
  function destroySwiperSp(){
    if(swiperSp){
      swiperSp.destroy();
      swiperSp = undefined;
    }
  }
  // if分を用いて横幅に応じて関数を実行
  function handleResize(){
    var w = window.innerWidth || document.documentElement.clientWidth || document.body.clientWidth;
    if(w > 768){
      if(!swiperPc){
        initializeSwiperPc();
      }
      if(swiperSp){
        destroySwiperSp();
      }
    }else{
      if(swiperPc){
        destroySwiperPc();
      }
      if(!swiperSp){
        initializeSwiperSp();
      }
    }
  }
  // 初回実行
  initializeSwiperAll();
  handleResize();
  window.addEventListener('resize', handleResize, initializeSwiperAll);
});
</script>
